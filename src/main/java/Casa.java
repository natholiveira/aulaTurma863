public class Casa {
    private Boolean possuiJanela;
    private Boolean possuiVaranda;

    public Casa(Boolean possuiJanela, Boolean possuiVaranda) {
        this.possuiJanela = possuiJanela;
        this.possuiVaranda = possuiVaranda;
    }

    public Boolean possuiJanela() {
        return possuiJanela;
    }

    public Boolean possuiVaranda() {
        return possuiJanela;
    }
}


